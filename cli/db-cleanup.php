{
    const DELETE = [
        'DELETE FROM players WHERE White="Andreikin,Dmitry:Muzychuk,Mariya" OR Black="Andreikin,Dmitry:Muzychuk,Mariya"',
        'DELETE FROM players WHERE White="Caruana,Fabiano:Green,Lee" OR Black="Caruana,Fabiano:Green,Lee"',
        'DELETE FROM players WHERE White="Giri,Anish:Vujatovic,Rajko" OR Black="Giri,Anish:Vujatovic,Rajko"',
        'DELETE FROM players WHERE White="Ivanishin,Anatoly :Ivan Vagner" OR Black="Ivanishin,Anatoly :Ivan Vagner"',
        'DELETE FROM players WHERE White="Jones,Gawain C B:Woodward,Clive" OR Black="Jones,Gawain C B:Woodward,Clive"',
        'DELETE FROM players WHERE White="Kramnik,Vladimir:Picot,Russell" OR Black="Kramnik,Vladimir:Picot,Russell"',
        'DELETE FROM players WHERE White="Nakamura,Hikaru:Hodgson,Jeremy" OR Black="Nakamura,Hikaru:Hodgson,Jeremy"',
        'DELETE FROM players WHERE White="Short,Nigel D:Baptie,Justin" OR Black="Short,Nigel D:Baptie,Justin"',
        'DELETE FROM players WHERE White="Xiangzhi,Bu:Xue Zhao" OR Black="Xiangzhi,Bu:Xue Zhao"',
        'DELETE FROM players WHERE White="Yang,Wen:Yifan,Hou" OR Black="Yang,Wen:Yifan,Hou"',
